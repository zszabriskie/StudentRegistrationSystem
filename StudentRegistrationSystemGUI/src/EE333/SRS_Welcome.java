    private void jButtonRegisterCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterCourseActionPerformed
        // TODO add your handling code here:
        String selectedStudentName = (String) jComboSCR.getSelectedItem();
        String selectedCourseName = (String) jComboCourseRegistration.getSelectedItem();

    if (jComboSCR.getItemCount() == 0 || jComboCourseRegistration.getItemCount() == 0) {
        jTextStatus.setText("Error: No students or courses available to register.");
    return;
}

boolean studentFound = false;
boolean courseFound = false;

for (EE333.StudentInfo student : Students) {
    String fullName = student.getFirstName() + " " + student.getLastName();
    if (fullName.equals(selectedStudentName)) {
        studentFound = true;

        for (EE333.CourseInfo course : Courses) {
            if (course.getCourseName().equals(selectedCourseName)) {
                courseFound = true;
                student.registerCourse(course);
                jTextCourseResitrationStatus.setText("Course registered successfully!");
                return;
            }
        }
    }
}

if (!studentFound) {
    jTextStatus.setText("Error: Student not found.");
} else if (!courseFound) {
    jTextStatus.setText("Error: Course not found.");
}

    }//GEN-LAST:event_jButtonRegisterCourseActionPerformed

    private void jButtonChangeStudentInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChangeStudentInfoActionPerformed
        // TODO add your handling code here:
        
   String selectedName = (String) jComboBoxFullname.getSelectedItem();
String newFirstName = jTextFieldNewFirstName.getText();
String newLastName = jTextFieldNewLastName.getText();
String newEmail = jTextFieldNewEmail.getText();

if (selectedName.length() == 0) {
    jTextStatus.setText("Error: No student selected.");
} else {
    for (EE333.StudentInfo student : Students) {
        String fullName = student.getFirstName() + " " + student.getLastName();
        if (fullName.equals(selectedName)) {
            student.setFirstName(newFirstName);
            student.setLastName(newLastName);
            student.setEmail(newEmail);
        }
    }

    // Clear all related combo boxes
    jComboBoxFullname.removeAllItems();
    jComboSCR.removeAllItems();
    jComboBoxObjects.removeAllItems();

    // Refill all with updated student names
    for (EE333.StudentInfo student : Students) {
        String updatedFullName = student.getFirstName() + " " + student.getLastName();
        jComboBoxFullname.addItem(updatedFullName);
        jComboSCR.addItem(updatedFullName);

        // If currently viewing Students in jComboBoxObjects, add the name
        if ("Students".equals(jComboBoxArrays.getSelectedItem().toString())) {
            jComboBoxObjects.addItem(updatedFullName);
        }
    }

    jTextStatus.setText("Student updated successfully.");
}

// Clear text fields
jTextFieldNewFirstName.setText("");
jTextFieldNewLastName.setText("");
jTextFieldNewEmail.setText("");


    }//GEN-LAST:event_jButtonChangeStudentInfoActionPerformed

    private void jButtonChangeCourseInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChangeCourseInfoActionPerformed
        // TODO add your handling code here:
    String selectedCourse = (String) jComboBoxCurrentCourseName.getSelectedItem();
    String newProfessor = jTextFieldNewProfessor.getText();
    String newCRN = jTextFieldNewCRN.getText();
    int newSeatCount = Integer.parseInt(jTextFieldNewSeatCount.getText());
    String newDescription = jTextFieldNewDescription.getText();

    if (selectedCourse.length() == 0) {
        jTextStatus.setText("Error: No course selected.");
    } else {
    for (EE333.CourseInfo course : Courses) {
        String courseName = course.getCourseName();
        if (courseName.equals(selectedCourse)) {
            course.setProfessor(newProfessor);
            course.setCRN(newCRN);
            course.setSeats(newSeatCount);
            course.setDescription(newDescription);
        }
    }

    jComboBoxCurrentCourseName.removeAllItems();
    jComboBoxObjects.removeAllItems();

    for (EE333.CourseInfo course : Courses) {
        String updatedCourseName = course.getCourseName();
        jComboBoxCurrentCourseName.addItem(updatedCourseName);

        if ("Courses".equals(jComboBoxArrays.getSelectedItem().toString())) {
            jComboBoxObjects.addItem(updatedCourseName);
        }
    }

    jTextStatus.setText("Course updated successfully.");
}

    jTextFieldNewProfessor.setText("");
    jTextFieldNewCRN.setText("");
    jTextFieldNewSeatCount.setText("");
    jTextFieldNewDescription.setText("");

    }//GEN-LAST:event_jButtonChangeCourseInfoActionPerformed

