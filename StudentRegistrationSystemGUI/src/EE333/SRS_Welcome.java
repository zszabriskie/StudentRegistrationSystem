    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        // TODO add your handling code here:

    String FirstName = this.jTextFirstName.getText();
    String LastName = this.jTextLastName.getText();
    String Email = this.jTextEmail.getText();
    String Level = this.jComboLevel.getSelectedItem().toString();
    boolean Contact = this.jCheckContact.isSelected();

    if (FirstName.length() == 0) {
        this.jTextStatus.setText("Error: Please enter a FirstName");
    } else if (LastName.length() == 0) {
        this.jTextStatus.setText("Error: Please enter a LastName");
    } else if (Email.length() == 0) {
        this.jTextStatus.setText("Error: Please enter a valid Email!");
    } else {
        UserLevel LevelAsEnum;
        if (Level.equalsIgnoreCase("Undergraduate")) {
            LevelAsEnum = UserLevel.Undergraduate;
        } else if (Level.equalsIgnoreCase("Graduate")) {
            LevelAsEnum = UserLevel.Graduate;
        } else {
            LevelAsEnum = UserLevel.Other;
        }

        EE333.StudentInfo NewStudent = new EE333.StudentInfo(FirstName, LastName, Email, LevelAsEnum);
        
        Students.add(NewStudent);

        this.jTextStatus.setText("Note: Successfully exported!");
        this.jTextID.setText(NewStudent.getID());
        this.jTextFirstName.setText("");
        this.jTextLastName.setText("");
        this.jTextEmail.setText("");
        this.jComboLevel.setSelectedIndex(0);
        this.jCheckContact.setSelected(false);
        
        jComboBoxFullname.removeAllItems();
        for (EE333.StudentInfo student : Students) {
        jComboBoxFullname.addItem(student.getFirstName() + " " + student.getLastName());
        jComboSCR.addItem(student.getFirstName() + " " + student.getLastName());
}
    
}

    }//GEN-LAST:event_jButtonSubmitActionPerformed

