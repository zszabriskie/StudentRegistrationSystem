    private void jButtonCreateCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateCourseActionPerformed
        // TODO add your handling code here:
        
    String CourseName = jTextFieldNewCourseName.getText();
    String Professor = jTextFieldNewCourseProfessor.getText();
    String CRN = jTextFieldCRN.getText();
    String Description = jTextFieldDescription.getText();
    String Seats = jTextFieldSeats.getText();

    if (CourseName.length() == 0) {
        jTextNewCourseStatus.setText("Error: Please enter a course name.");
    } else if (Professor.length() == 0) {
        jTextNewCourseStatus.setText("Error: Please enter a professor.");
    } else if (CRN.length() == 0) {
        jTextNewCourseStatus.setText("Error: Please enter a CRN.");
    } else if (Description.length() == 0) {
        jTextNewCourseStatus.setText("Error: Please enter a description.");
    } else if (Seats.length() == 0) {
        jTextNewCourseStatus.setText("Error: Please enter the number of seats.");
    } else {
        int seats = Integer.parseInt(Seats);

    CourseInfo NewCourse = new CourseInfo(CourseName, Professor, CRN, Description, seats);
    
    Courses.add(NewCourse);
    
    for (EE333.CourseInfo course : Courses) {
        jComboCourseRegistration.addItem(course.getCourseName());
        jComboBoxCurrentCourseName.addItem(course.getCourseName());
}

    jTextNewCourseStatus.setText("Course created successfully!");

    jTextFieldNewCourseName.setText("");
    jTextFieldNewCourseProfessor.setText("");
    jTextFieldCRN.setText("");
    jTextFieldDescription.setText("");
    jTextFieldSeats.setText("");
    }
        
    }//GEN-LAST:event_jButtonCreateCourseActionPerformed

